package com.example.someapp.service;

import java.util.List;
import java.util.Set;

/**
 * Простой стеммер для ингушского языка.
 *
 * Стратегия: пробуем отрезать суффиксы от длинных к коротким.
 * Если оставшаяся основа есть в словаре — слово считается валидным.
 *
 * Источник суффиксов: Johanna Nichols, «Ingush Grammar» (2011),
 * главы 6 (склонение), 8 (словообразование) + эмпирический анализ словаря.
 *
 * Покрытие (~70% флектированных форм, без аблаутных чередований).
 * Аблаутные формы (бер→биера, куор→куора) не покрываются — требуют
 * отдельного морфологического словаря.
 */
public class IngushStemmer {

    private static final int MIN_STEM_LENGTH = 2;

    /**
     * Суффиксы упорядочены от длинных к коротким.
     * Категории:
     *   1. Падежи множественного числа (ашта, ашка, ...)
     *   2. Множественное число (аш, ий, ой, ...)
     *   3. Падежные суффиксы единственного числа (ага, аца, нна, ал, ...)
     *   4. Короткие падежные суффиксы (га, ца, хь, ах, ...)
     */
    private static final List<String> SUFFIXES = List.of(

        // — Мн.ч. + падеж (6 символов) —
        "ашкахьа",   // аллатив мн.ч. + локатив (бераш-ка-хьа)

        // — Мн.ч. + падеж (5 символов) —
        "аьшта",     // дательный мн.ч. (аьш-та)
        "ашкахь",    // локатив мн.ч.
        "аьшка",     // аллатив мн.ч.
        "аьшца",     // инструментальный мн.ч.

        // — Мн.ч. + падеж (4 символа) —
        "ашта",      // дательный мн.ч. (аш-та): берашта
        "ашка",      // аллатив мн.ч. (аш-ка): берашка
        "ашца",      // инструментальный мн.ч.
        "ашхь",      // локатив мн.ч.
        "гӏча",      // конверб/постпозиция: дикагӏча

        // — Мн.ч. именительный (3 символа) —
        "аьш",       // мн.ч. с умлаутом
        "аш",        // мн.ч. (самый частотный): бераш, куотамаш

        // — Множ. число с расширением (3 символа) —
        "рч",        // мн.ч. животных: черч (медведи ← ча)

        // — Падежные суффиксы ед.ч. с расширением (3–4 символа) —
        "нна",       // дательный с -н- расширением: дӏабенна
        "ага",       // аллатив: куотамага, берага
        "аца",       // инструментальный: куотамаца
        "гӏа",       // аллатив (с палочкой): дикагӏа, кхерагӏа
        "хьа",       // директив/послелог: айрахьа
        "ара",       // аблатив: вӏашагӏтохара
        "ача",       // конверб (причастный оборот): арадовлача
        "ана",       // дательный: молланна ~ моллана
        "нах",       // постпозиционный генитив (очень частый): берийнах, нахнах

        // — Множественное число (2 символа) —
        "ий",        // мн.ч. одушевлённых: аьленний
        "ой",        // мн.ч. с -хьуо суффиксом: моарой

        // — Падежные суффиксы ед.ч. (2 символа) —
        "га",        // аллатив краткий: хъога, дикага
        "ца",        // инструментальный краткий: духтӏаваларца
        "ал",        // сравнительный (согл. основа): берал
        "ах",        // локатив/постпозиция: яхарах, берах
        "на",        // дательный краткий: пхона
        "хь",        // локатив: цӏенгахь
        "ан",        // генитив: гӏален → гӏала
        "аа",        // дательный (долгое -аа): куотамаа
        "й",         // генитив жӏали: жӏалий → жӏали
        "л"          // сравнительный (гласн. основа): вошал → воша, нийсал → нийса
    );

    /**
     * Возвращает true, если слово является известной флектированной формой
     * какого-либо слова из словаря.
     */
    public boolean isKnownForm(String word, Set<String> dictionary) {
        for (String suffix : SUFFIXES) {
            if (word.endsWith(suffix)) {
                String stem = word.substring(0, word.length() - suffix.length());
                if (stem.length() >= MIN_STEM_LENGTH && dictionary.contains(stem)) {
                    return true;
                }
            }
        }
        return false;
    }

    /**
     * Возвращает основу слова (первый найденный вариант), или само слово.
     */
    public String stem(String word, Set<String> dictionary) {
        for (String suffix : SUFFIXES) {
            if (word.endsWith(suffix)) {
                String stem = word.substring(0, word.length() - suffix.length());
                if (stem.length() >= MIN_STEM_LENGTH && dictionary.contains(stem)) {
                    return stem;
                }
            }
        }
        return word;
    }
}
